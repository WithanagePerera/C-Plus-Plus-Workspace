// Stores elements in key/value pairs
// Reference key to retrieve values
// Implemented using Hash Table

#include <iostream>
#include <string>
#include <unordered_map>

using namespace std;

int main()
{
    // Initialize unordered map
    unordered_map<int, string> speed;
    
    // Insert values
    speed.insert({1, "C"});
    speed.insert({2, "C++"});
}